//   完全保留重采样结果（不做段内插值——坡道会扭曲相对价，教训见 PARAMS 注）。
//   动机（初值表 v0 §13 诊断）：单 γ 下"中后期变富 → γ 全曲线下调 → 前段变便宜 →
//   肝档提前冲到 n060 墙下多等"；分段锚把每段各自钉死，收入形变只被本段 γ 吸收。
